package epam2;

/**
 * The class calculator has 3 methods sum, product, divison which perform operation of calculator
 * @author ashish
 *
 */

public class calculator {
	
	/**
	 * This method will add two numbers and return their sum as result
	 * @param a
	 * @param b
	 */
	
	public static int sum(int a, int b)
	{
		return (a+b);
	}
	
	/**
	 * This method will add two numbers and return their product as result
	 * @param a
	 * @param b
	 */
	
	public static int product(int a, int b)
	{
		return (a*b);
	}
	
	/**
	 * This method will add two numbers and return their divison as result
	 * @param a
	 * @param b
	 */
	
	public static int divison(int a, int b)
	{
		return (a/b);
	}

}
